/** Full Meriter Telegram user guide (HTML parse_mode). */

import { primaryCommunityHashtag } from './telegram-messages.ru';

export type TelegramGuideInput = {
  hashtags?: string[];
  /** When true, copy describes button panel voting; otherwise reactions. */
  votePanelEnabled?: boolean;
};

function esc(text: string): string {
  return text.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;');
}

function b(text: string): string {
  return `<b>${esc(text)}</b>`;
}

function i(text: string): string {
  return `<i>${esc(text)}</i>`;
}

function buildGuideHashtagSection(hashtag: string): string[] {
  return [
    b('3. КАК ПОЛУЧИТЬ ЗАСЛУГИ'),
    '',
    '1) Напишите в группе сообщение с заданным хэштегом – например:',
    `#${hashtag} Предлагаю собраться в субботу`,
    '',
    i(`(Хэштег может быть другой — лид задаёт в настройках. Часто это #${hashtag}.)`),
    '',
    'Другие голосуют за это сообщение, вы получаете заслуги.',
    '',
    'Заслуги другому участнику:',
    `• ответьте на его сообщение и напишите #${hashtag} … (заслуги пойдут ему),`,
    `• или «#${hashtag} для @username …» без reply.`,
  ];
}

function buildGuideVotingSection(votePanelEnabled?: boolean): string[] {
  const header = [b('4. КАК ГОЛОСОВАТЬ'), '', 'Голосовать можно только за сообщения с хэштегом. Без хэштега голос не засчитается.', ''];

  if (votePanelEnabled) {
    return [
      ...header,
      'Под постами — кнопки начисления заслуг:',
      '• +1 / +3 / +5 — начислить заслуги (на +1 — сумма всех поддержек)',
      '• Своя сумма — ввести число',
      '• Против — списать заслуги с получателя',
      '',
      'Счётчики на кнопках показывают, сколько заслуг начислено или списано.',
      '',
      'Нельзя голосовать за свой пост и за пост, где вы получатель заслуг.',
    ];
  }

  return [
    ...header,
    'Голосуйте реакциями:',
    '',
    '👍 — быстро +1 заслуга автору',
    '❤️ — поддержать сильнее: бот в группе (ответом на сообщение, за которое голосуете) спросит сумму и покажет кнопки 1 / 3 / 5. Обращение будет к вам по имени.',
    i('Через минуту запрос бота исчезнет — это нормально. Итоговый отчёт останется в чате.'),
    '👎 — минус автору (тоже выбор суммы, списание с вашего кошелька)',
    '',
    'Или ответьте на пост текстом: +3 Отличная работа или -2 Не согласен',
    '',
    'Нельзя голосовать за свой пост и за пост, где вы получатель заслуг.',
  ];
}

function buildGuideCheatSheet(hashtag: string, votePanelEnabled?: boolean): string[] {
  const voteLine = votePanelEnabled
    ? 'Голос → кнопки под постом (+1, своя сумма, против)'
    : 'Голос → 👍 / ❤️ / 👎 или +3 / -2';

  return [
    b('9. ШПАРГАЛКА'),
    '',
    `Пост → #${hashtag} ваш текст (или reply + #${hashtag} — заслуги тому, на чьё сообщение ответили)`,
    voteLine,
    'Баланс в чате → /balance',
    'Мини-апп → /link или ссылка в закреплённом сообщении',
    'Поменять настройки → /settings',
    'Подробный гайд → /guide',
  ];
}

